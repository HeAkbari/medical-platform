/* eslint-disable no-console */
/**
 * Workaround for Nx Console + Nx 22.x so "Run Target" uses `bun nx` instead of `npx nx`.
 * Copied from easytrader workspace pattern.
 */
const fs = require('node:fs');
const path = require('node:path');

const workspaceRoot = path.resolve(__dirname, '..');
const nxRoot = path.join(workspaceRoot, 'node_modules', 'nx');
const realFile = path.join(nxRoot, 'dist', 'src', 'utils', 'package-manager.js');
const stubDir = path.join(nxRoot, 'src', 'utils');
const stubFile = path.join(stubDir, 'package-manager.js');

if (!fs.existsSync(nxRoot)) {
  console.log('[patch-nx-console-pm] skipped: node_modules/nx not present');
  process.exit(0);
}

if (!fs.existsSync(realFile)) {
  console.log(
    `[patch-nx-console-pm] skipped: real module missing at ${path.relative(workspaceRoot, realFile)}`
  );
  process.exit(0);
}

fs.mkdirSync(stubDir, { recursive: true });
fs.writeFileSync(
  stubFile,
  `// Auto-generated by scripts/patch-nx-console-pm.js. Do not edit.\n` +
    `module.exports = require('../../dist/src/utils/package-manager');\n`,
  'utf8'
);

console.log(
  `[patch-nx-console-pm] wrote stub at ${path.relative(workspaceRoot, stubFile)}`
);
